# 案例：输入两个数 x 和 y ，计算 x + y 以及 x - y 的结果并输出
# float(..) ---> 将其他类型 转为 浮点数类型
x = float(input("请输入x的值："))
y = float(input("请输入y的值："))

# 0.09999999999999998 --> 精度损失 ；由于计算机底层是基于 二进制 进行数据的存储与处理，二进制是无法准确的表示所有的小数，因此涉及到浮点数的运算，可能会存在精度损失
print("x + y = ", x + y)
print("x - y = ", x - y)

# # # # # # # # # # # 赋值运算符：= += -= *= /= %= //= **=  # # # # # # # # # # #
